package caeser;

public class CryptoAlphabet {
    private static final String lettersUpperCase = "ABCDEFGHIKLMNOPRSTUVWXYZ";
    private static final String lettersLowerCase = "abcdefghiklmnoprstuvwxyz";
    private static final String numbers = "0123456789";
    private static final String symbols = ".,?!%#@*&+-=";
    public static final String alphabet = lettersUpperCase + lettersLowerCase + symbols + numbers;
}
